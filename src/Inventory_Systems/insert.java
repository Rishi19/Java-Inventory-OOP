    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        Product product = new Product();
        product.setCode(jTextField2.getText());
        product.setDesc(jTextField1.getText());
        product.setCost((float) Double.parseDouble(jTextField3.getText()));
        
        boolean result=product.addProduct(product);
        
        if(result){
             JOptionPane.showMessageDialog(null,"Data Saved Succefully");
        }
        else{
            JOptionPane.showMessageDialog(null,"Data Not Saved Succefully");
        }
        
        Show_products_In_JTable();
    }//GEN-LAST:event_jButton1MouseClicked

